# Inteiro

numero = 3
print(f"Inteiro = {numero}")
print(type(numero))

# ponto flutuante

numero_float = 3.5
print(type(numero_float))

# Soma +

soma = 1 + 1
print(f"Soma = {soma}")

# Multiplicação

multi = 2 * 2
print(f"Multiplicacao = {multi}")

# Divisão

divisao = 10 / 3
print(f"Divisão = {divisao:.2f}")

# Float
print(f"Soma = {float(soma)}")

# Módulo %
modulo = 25 % 2
print(modulo)

